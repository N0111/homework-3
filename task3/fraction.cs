class Fraction
{
    private double a, b;



    public Fraction(double a, double b)
    {
        this.a = a;
        this.b = b;
    }

    public double Sum()
    {
        return a + b;
    }
    public double Sub()
    {
        return a - b;
    }
     public double Mul()
    {
        return a * b;
    }
         public double Sha()
    {
        return a / b;
    }

}